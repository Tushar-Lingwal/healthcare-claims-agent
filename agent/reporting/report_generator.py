class ReportGenerator:
    """
    Generates a complete structured ClinicalReport from an AdjudicationResult.

    Usage:
        gen    = ReportGenerator()
        report = gen.generate(result, claim)
        html   = report.to_html()
        text   = report.to_text()
        data   = report.to_dict()
    """

    def generate(
        self,
        result: AdjudicationResult,
        claim:  Optional[TokenizedClaimInput] = None,
        audit_entries: Optional[list] = None,
    ) -> ClinicalReport:
        """
        Generates a full ClinicalReport from an adjudication result.

        Args:
            result:        AdjudicationResult from the pipeline
            claim:         TokenizedClaimInput (for patient metadata)
            audit_entries: list of AuditEntry from get_audit_logger().get_trace()
        """
        now     = datetime.utcnow()
        report_id = f"RPT-{result.claim_id}-{now.strftime('%Y%m%d%H%M%S')}"

        dec        = result.decision.value
        conf       = result.confidence_score
        conf_pct   = f"{conf:.0%}"
        blocking   = [ev for ev in result.rule_evaluations if not ev.passed and ev.action.value == "reject"]
        warnings   = [ev for ev in result.rule_evaluations if not ev.passed and ev.action.value == "flag_review"]
        edge_cases = result.edge_cases or []
        has_mismatch = any(ec.edge_case_type.value == "image_text_mismatch" for ec in edge_cases)

        report = ClinicalReport(
            report_id       = report_id,
            generated_at    = now,
            report_version  = REPORT_VERSION,
            claim_id        = result.claim_id,
            audit_trace_id  = result.audit_trace_id,
            decided_at      = result.decided_at,
            decision        = dec,
            confidence_score= conf,
            decision_colour = "#0a7c52" if dec=="approved" else ("#c41c3a" if dec=="rejected" else "#92400e"),
            executive_summary   = self._build_executive_summary(result, blocking, warnings, edge_cases, conf_pct, claim),
            patient_metadata    = self._build_patient_metadata(result, claim),
            clinical_findings   = self._build_clinical_findings(result),
            code_analysis       = self._build_code_analysis(result),
            rule_evaluation     = self._build_rule_evaluation(result),
            edge_case_analysis  = self._build_edge_case_analysis(result),
            guideline_citations = self._build_guideline_citations(result),
            imaging_analysis    = self._build_imaging_analysis(result),
            risk_assessment     = self._build_risk_assessment(result, blocking, edge_cases, has_mismatch),
            recommended_actions = self._build_recommended_actions(result, blocking, warnings, edge_cases, has_mismatch, claim),
            audit_trail         = self._build_audit_trail(result, audit_entries),
            report_signature    = self._build_signature(result, report_id, now),
        )

        # Attach reasoning chain for HTML rendering
        if result.explainability and result.explainability.reasoning_chain:
            report._reasoning_chain = [
                {
                    "stage":      step.stage.value.replace("_"," ").title(),
                    "confidence": f"{step.confidence:.0%}",
                    "outcome":    step.outcome,
                }
                for step in result.explainability.reasoning_chain
            ]
        else:
            report._reasoning_chain = []

        return report

