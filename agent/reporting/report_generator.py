    def _build_executive_summary(self, result, blocking, warnings, edge_cases, conf_pct, claim) -> ReportSection:
        dec  = result.decision.value
        plan = claim.patient_info.insurance_plan.value if claim else "unknown"
        n_rules = len(result.rule_evaluations)

        if dec == "approved":
            narrative = (
                f"This claim has been approved with {conf_pct} confidence. "
                f"All {n_rules} applicable policy rule(s) passed and no clinical anomalies were detected. "
                f"The submitted diagnosis and procedure codes are medically consistent and are covered under the patient's {plan} insurance plan. "
                f"No further action is required — this claim may proceed to payment processing."
            )
            items = [{"value": "All policy rules passed with no blocking failures."},
                     {"value": f"Coded {len(result.icd10_codes)} ICD-10 diagnosis(es) and {len(result.cpt_codes)} CPT procedure(s)."},
                     {"value": f"No edge cases or clinical anomalies detected."}]

        elif dec == "rejected":
            primary = blocking[0] if blocking else None
            rule_detail = f"Rule {primary.rule_id}: {primary.reason}" if primary else "Policy rule failure"
            narrative = (
                f"This claim has been rejected with {conf_pct} confidence. "
                f"The primary rejection reason is: {rule_detail}. "
                f"The claim cannot be approved in its current form and requires correction before resubmission. "
                f"Please review the Required Actions section below for specific steps to resolve this rejection."
            )
            items = [{"value": f"Blocking failures: {len(blocking)} rule(s) triggered a REJECT action."}]
            if warnings:
                items.append({"value": f"Additional warnings: {len(warnings)} rule(s) flagged for review."})

        else:  # needs_review
            reasons = []
            if warnings:   reasons.append(f"{len(warnings)} policy rule(s) require payer verification")
            if edge_cases: reasons.append(f"{len(edge_cases)} clinical anomaly(ies) detected")
            reason_str = "; ".join(reasons) if reasons else "policy concerns requiring manual evaluation"
            narrative = (
                f"This claim has been flagged for manual review with {conf_pct} confidence. "
                f"Issues identified: {reason_str}. "
                f"A qualified clinical reviewer must evaluate this claim before a final adjudication decision can be made. "
                f"Do not process for payment until review is complete."
            )
            items = [{"value": r} for r in result.reasons[:5]]

        return ReportSection(title="Executive Summary", content=narrative, items=items)

    def _build_patient_metadata(self, result, claim) -> ReportSection:
        items = [
            {"label": "Claim ID",       "value": result.claim_id},
            {"label": "Audit Trace",    "value": result.audit_trace_id},
            {"label": "Decided At",     "value": result.decided_at.strftime("%Y-%m-%d %H:%M UTC")},
            {"label": "Pipeline Ver.",  "value": result.pipeline_version},
        ]
        if claim:
            pi = claim.patient_info
            items += [
                {"label": "Patient ID",    "value": pi.token_patient_id},
                {"label": "Age Band",      "value": pi.age_band},
                {"label": "Sex",           "value": pi.sex},
                {"label": "Plan",          "value": pi.insurance_plan.value.upper()},
                {"label": "Policy Token",  "value": pi.token_policy},
            ]
        return ReportSection(title="Patient & Claim Information", content="", items=items)

    def _build_clinical_findings(self, result) -> ReportSection:
        items = []
        # We get entities from the coding result since extraction result not on AdjudicationResult
        if result.icd10_codes:
            items.append({"label": "Diagnosed Conditions", "values": [f"{c.code}: {c.description}" for c in result.icd10_codes]})
        if result.cpt_codes:
            items.append({"label": "Procedures Identified", "values": [f"{c.code}: {c.description}" for c in result.cpt_codes]})
        if result.edge_cases:
            items.append({"label": "Clinical Anomalies", "values": [ec.edge_case_type.value.replace("_"," ").title() for ec in result.edge_cases]})

        content = result.explainability.summary if result.explainability else ""
        return ReportSection(title="Clinical Findings", content=content, items=items)

    def _build_code_analysis(self, result) -> ReportSection:
        items = []
        for c in result.icd10_codes:
            items.append({
                "system":      "ICD-10-CM",
                "code":        c.code,
                "description": c.description,
                "confidence":  f"{c.confidence:.0%}",
                "exact":       "exact match" if c.is_exact_match else "fuzzy match",
            })
        for c in result.cpt_codes:
            items.append({
                "system":      "CPT",
                "code":        c.code,
                "description": c.description,
                "confidence":  f"{c.confidence:.0%}",
                "exact":       "exact match" if c.is_exact_match else "fuzzy match",
            })
        content = f"{len(result.icd10_codes)} ICD-10-CM diagnosis code(s) and {len(result.cpt_codes)} CPT procedure code(s) mapped."
        return ReportSection(title="Medical Code Analysis", content=content, items=items)

    def _build_rule_evaluation(self, result) -> ReportSection:
        items = []
        for ev in result.rule_evaluations:
            if ev.passed:
                status = "pass"
            elif ev.action.value == "reject":
                status = "fail"
            else:
                status = "warn"
            items.append({
                "rule_id": ev.rule_id,
                "status":  status,
                "action":  ev.action.value,
                "reason":  ev.reason,
                "label":   ev.rule_name if hasattr(ev, "rule_name") else ev.rule_id,
                "value":   ev.reason,
            })
        n_pass = sum(1 for ev in result.rule_evaluations if ev.passed)
        n_fail = len(result.rule_evaluations) - n_pass
        content = f"{len(result.rule_evaluations)} rules evaluated — {n_pass} passed, {n_fail} failed."
        return ReportSection(title="Policy Rule Evaluation", content=content, items=items)

    def _build_edge_case_analysis(self, result) -> ReportSection:
        items = []
        for ec in result.edge_cases:
            items.append({
                "type":         ec.edge_case_type.value,
                "severity":     ec.severity.value,
                "description":  ec.description,
                "recommendation": ec.recommendation if hasattr(ec,"recommendation") else "",
                "image_signal": ec.image_signal if hasattr(ec,"image_signal") else None,
                "text_signal":  ec.text_signal  if hasattr(ec,"text_signal")  else None,
                "mismatch_score": f"{ec.mismatch_score:.2f}" if hasattr(ec,"mismatch_score") and ec.mismatch_score else None,
            })
        content = f"{len(result.edge_cases)} edge case(s) detected." if result.edge_cases else "No edge cases detected."
        return ReportSection(title="Edge Case Analysis", content=content, items=items)

    def _build_guideline_citations(self, result) -> ReportSection:
        items = []
        passages = result.rag_passages or []
        for p in passages[:5]:
            items.append({
                "source":  p.source,
                "passage_id": p.passage_id if hasattr(p,"passage_id") else "",
                "relevance": f"{p.relevance:.2f}",
                "excerpt": p.content[:250] + "..." if len(p.content) > 250 else p.content,
            })
        content = f"{len(passages)} clinical guideline passage(s) retrieved and evaluated."
        return ReportSection(title="Clinical Guideline Citations", content=content, items=items)

    def _build_imaging_analysis(self, result) -> ReportSection:
        img = result.imaging_result
        if not img:
            return ReportSection(title="Imaging Analysis", content="No imaging analysis was performed for this claim. Imaging mode was set to SKIPPED.", items=[])

        content = (
            f"Imaging analysis was performed using the {img.mode_used.value} model. "
            f"Classification result: '{img.class_label}' with {img.confidence:.0%} confidence."
        )
        if img.icd10_suggestion:
            content += f" Suggested ICD-10 code: {img.icd10_suggestion}."

        items = [
            {"label": "Mode",        "value": img.mode_used.value},
            {"label": "Class Label", "value": img.class_label or "—"},
            {"label": "Confidence",  "value": f"{img.confidence:.0%}"},
            {"label": "ICD-10 Hint", "value": img.icd10_suggestion or "—"},
        ]
        return ReportSection(title="Imaging Analysis", content=content, items=items)

    def _build_risk_assessment(self, result, blocking, edge_cases, has_mismatch) -> ReportSection:
        dec  = result.decision.value
        risk_items = []

        if dec == "approved":
            content = "Risk level: LOW. This claim passed all automated checks and is cleared for payment processing. Standard post-payment audit procedures apply."
        elif dec == "rejected":
            content = f"Risk level: HIGH. This claim contains {len(blocking)} blocking policy violation(s) that prevent approval. Resubmission will be required after the issues identified below are resolved."
            for ev in blocking:
                risk_items.append({"severity": "high", "text": f"BLOCKING — {ev.rule_id}: {ev.reason}"})
        else:
            content = "Risk level: MEDIUM. This claim requires human review before a payment decision can be made. The following issues were identified:"
            for ev in result.rule_evaluations:
                if not ev.passed:
                    risk_items.append({"severity": "medium", "text": f"Rule {ev.rule_id}: {ev.reason}"})

        for ec in edge_cases:
            sev = ec.severity.value.lower()
            risk_items.append({"severity": sev, "text": f"{ec.edge_case_type.value}: {ec.description}"})

        if has_mismatch:
            risk_items.append({"severity": "high", "text": "IMAGE_TEXT_MISMATCH: Imaging and clinical text diagnoses disagree — specialist review required before any payment decision."})

        return ReportSection(title="Risk Assessment", content=content, items=risk_items)

    def _build_recommended_actions(self, result, blocking, warnings, edge_cases, has_mismatch, claim) -> ReportSection:
        dec     = result.decision.value
        plan    = claim.patient_info.insurance_plan.value if claim else "unknown"
        items   = []
        n       = 1

        if dec == "approved":
            items.append({"priority":"info",     "text": f"Proceed to payment processing — all {len(result.rule_evaluations)} policy checks passed and no anomalies detected."})
            items.append({"priority":"info",     "text": f"File audit record <strong>{result.audit_trace_id}</strong> with the claim documentation for compliance purposes."})
            items.append({"priority":"low",      "text": "No further clinical review required. Standard post-payment audit procedures apply."})
            if result.rag_passages:
                items.append({"priority":"low",  "text": f"Clinical necessity supported by {len(result.rag_passages)} guideline passage(s) — see Citations section."})

        elif dec == "rejected":
            for ev in blocking:
                rule_id = ev.rule_id
                reason  = ev.reason
                if rule_id.startswith("COMPAT"):
                    items.append({"priority":"critical", "text": f"<strong>{rule_id}</strong>: Obtain and attach supporting clinical documentation establishing the required diagnosis-procedure relationship. {reason}"})
                elif rule_id.startswith("COV"):
                    items.append({"priority":"critical", "text": f"<strong>{rule_id}</strong>: Coverage exclusion under {plan.upper()} plan. Options: (a) upgrade patient to STANDARD or PREMIUM plan, (b) seek a coverage exception with medical necessity documentation, (c) bill the patient directly if applicable. {reason}"})
                elif rule_id.startswith("AGE"):
                    items.append({"priority":"critical", "text": f"<strong>{rule_id}</strong>: Age restriction applies. If a clinical exception exists, obtain specialist documentation and written justification. {reason}"})
                else:
                    items.append({"priority":"critical", "text": f"<strong>{rule_id}</strong>: {reason}"})
            items.append({"priority":"high",  "text": "Correct ALL identified issues before resubmission. Partial corrections will result in re-rejection."})
            items.append({"priority":"medium","text": f"Reference audit trace <strong>{result.audit_trace_id}</strong> in all correspondence and resubmission documentation."})
            items.append({"priority":"low",   "text": "If you believe this rejection is in error, initiate the payer appeals process with supporting clinical evidence."})

        else:  # needs_review
            for ev in warnings:
                if ev.rule_id.startswith("AUTH"):
                    items.append({"priority":"high",   "text": f"<strong>{ev.rule_id}</strong>: Submit prior authorization request to the payer BEFORE claim payment. Include: diagnosis codes, clinical indication, treating physician details, and prior treatment history. {ev.reason}"})
                else:
                    items.append({"priority":"medium", "text": f"<strong>{ev.rule_id}</strong>: {ev.reason}"})

            for ec in edge_cases:
                sev  = ec.severity.value.lower()
                pri  = "critical" if sev in ("critical","high") else "medium"
                if ec.edge_case_type.value == "image_text_mismatch":
                    items.append({"priority":"critical", "text": f"<strong>IMAGING MISMATCH</strong>: Imaging suggests '{ec.image_signal}' but clinical text suggests '{ec.text_signal}' (mismatch score: {ec.mismatch_score:.2f}). Radiologist review and treating physician attestation required before adjudication."})
                elif ec.edge_case_type.value == "missing_diagnosis":
                    items.append({"priority":"critical", "text": f"<strong>MISSING DIAGNOSIS</strong>: {ec.description} Submit ICD-10 diagnosis codes supporting medical necessity for all claimed procedures."})
                elif ec.edge_case_type.value == "low_confidence_extraction":
                    items.append({"priority":"medium",  "text": f"<strong>LOW CONFIDENCE</strong>: {ec.description} Clinical reviewer should verify extracted codes against source documentation."})
                else:
                    items.append({"priority":pri, "text": f"<strong>{ec.edge_case_type.value}</strong>: {ec.description} {ec.recommendation if hasattr(ec,'recommendation') else ''}"})

            items.append({"priority":"high",   "text": "Route to clinical reviewer queue immediately. Do NOT process for payment until review is complete and approved."})
            items.append({"priority":"medium", "text": f"Target review turnaround: 48 business hours. Reference audit trace <strong>{result.audit_trace_id}</strong>."})

        return ReportSection(title="Recommended Actions", content="", items=items)

    def _build_audit_trail(self, result, audit_entries) -> ReportSection:
        items = []
        if audit_entries:
            for entry in audit_entries:
                items.append({
                    "stage":       entry.stage.value if hasattr(entry.stage,"value") else str(entry.stage),
                    "status":      entry.status.value if hasattr(entry.status,"value") else str(entry.status),
                    "timestamp":   entry.timestamp.isoformat() if hasattr(entry,"timestamp") else "",
                    "duration_ms": entry.duration_ms if hasattr(entry,"duration_ms") else 0,
                    "error":       entry.error_message or "",
                })
        elif result.explainability and result.explainability.reasoning_chain:
            for step in result.explainability.reasoning_chain:
                items.append({
                    "stage":       step.stage.value.replace("_"," ").title(),
                    "status":      "SUCCESS",
                    "timestamp":   result.decided_at.isoformat(),
                    "duration_ms": "—",
                    "error":       "",
                })
        content = f"{len(items)} pipeline stage(s) recorded. No PHI present in audit trail."
        return ReportSection(title="Pipeline Audit Trail", content=content, items=items)

