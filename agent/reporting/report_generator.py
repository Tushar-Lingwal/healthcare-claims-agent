    def to_text(self) -> str:
        """Plain text report — suitable for email or logging."""
        lines = []
        w = 68

        def line(char="─"): lines.append(char * w)
        def header(text):   lines.append(f"  {text}")
        def blank():        lines.append("")

        line("═")
        header("CLAIMIQ — HEALTHCARE CLAIMS ADJUDICATION REPORT")
        line("═")
        header(f"Report ID    : {self.report_id}")
        header(f"Generated    : {self.generated_at.strftime('%Y-%m-%d %H:%M:%S UTC')}")
        header(f"Claim ID     : {self.claim_id}")
        header(f"Audit Trace  : {self.audit_trace_id}")
        header(f"Decided      : {self.decided_at.strftime('%Y-%m-%d %H:%M:%S UTC')}")
        line("═")
        blank()

        sections = [
            self.executive_summary, self.patient_metadata, self.clinical_findings,
            self.code_analysis, self.rule_evaluation, self.edge_case_analysis,
            self.guideline_citations, self.imaging_analysis, self.risk_assessment,
            self.recommended_actions, self.audit_trail, self.report_signature,
        ]

        for section in sections:
            if not section.content and not section.items:
                continue
            lines.append(f"  ── {section.title.upper()} {'─' * max(2, w - len(section.title) - 6)}")
            if section.content:
                for para in section.content.split("\n"):
                    lines.append(f"  {para}")
            if section.items:
                for item in section.items:
                    if isinstance(item, dict):
                        for k, v in item.items():
                            if v:
                                lines.append(f"    {k}: {v}")
                    blank()
            blank()

        line("═")
        lines.append("  END OF REPORT — CLAIMIQ HEALTHCARE CLAIMS ADJUDICATION AGENT")
        line("═")
        return "\n".join(lines)

    def to_markdown(self) -> str:
        """Markdown report — for GitHub, documentation, or rendering."""
        lines = []

        dec_emoji = {"approved": "✅", "rejected": "❌", "needs_review": "⚠️"}.get(self.decision, "📋")
        conf_pct  = f"{self.confidence_score:.0%}"

        lines.append(f"# {dec_emoji} ClaimIQ Adjudication Report")
        lines.append(f"")
        lines.append(f"| Field | Value |")
        lines.append(f"|---|---|")
        lines.append(f"| Report ID | `{self.report_id}` |")
        lines.append(f"| Generated | {self.generated_at.strftime('%Y-%m-%d %H:%M UTC')} |")
        lines.append(f"| Claim ID | `{self.claim_id}` |")
        lines.append(f"| Audit Trace | `{self.audit_trace_id}` |")
        lines.append(f"| **Decision** | **{self.decision.upper().replace('_',' ')}** |")
        lines.append(f"| Confidence | {conf_pct} |")
        lines.append("")

        sections = [
            self.executive_summary, self.clinical_findings, self.code_analysis,
            self.rule_evaluation, self.edge_case_analysis, self.risk_assessment,
            self.recommended_actions, self.guideline_citations,
        ]

        for section in sections:
            if not section.content and not section.items:
                continue
            lines.append(f"## {section.title}")
            if section.content:
                lines.append(section.content)
            if section.items:
                for item in section.items:
                    if isinstance(item, dict):
                        label = item.get("label", "")
                        value = item.get("value", "")
                        status= item.get("status", "")
                        if status == "pass":
                            lines.append(f"- ✅ **{label}**: {value}")
                        elif status == "fail":
                            lines.append(f"- ❌ **{label}**: {value}")
                        elif status == "warn":
                            lines.append(f"- ⚠️ **{label}**: {value}")
                        else:
                            lines.append(f"- **{label}**: {value}" if label else f"- {value}")
            lines.append("")

        return "\n".join(lines)

